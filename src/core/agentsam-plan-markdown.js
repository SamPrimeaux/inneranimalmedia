/**
 * Canonical Markdown export for agentsam_plans + agentsam_plan_tasks (no AI).
 */

function parseJsonArr(v) {
  if (v == null) return [];
  if (Array.isArray(v)) return v.map((x) => String(x).trim()).filter(Boolean);
  if (typeof v === 'string') {
    try {
      const p = JSON.parse(v);
      return Array.isArray(p) ? p.map((x) => String(x).trim()).filter(Boolean) : [];
    } catch {
      return [];
    }
  }
  return [];
}

function planGoalBlock(plan) {
  if (!plan) return '';
  const mb = plan.morning_brief;
  if (mb && typeof mb === 'string') {
    try {
      const o = JSON.parse(mb);
      if (o && typeof o.goal === 'string' && o.goal.trim()) return o.goal.trim();
    } catch {
      return mb.trim();
    }
    return mb.trim();
  }
  if (plan.session_notes) return String(plan.session_notes).trim();
  return '';
}

/** Escape cell text for GFM pipe tables */
function mdCell(s) {
  return String(s ?? '')
    .replace(/\|/g, '\\|')
    .replace(/\r?\n/g, ' ')
    .trim();
}

/**
 * @param {{ plan: Record<string, unknown>, tasks: Array<Record<string, unknown>> }} p
 * @returns {string}
 */
export function buildPlanMarkdown(p) {
  const plan = p.plan || {};
  const rawTasks = Array.isArray(p.tasks) ? [...p.tasks] : [];
  rawTasks.sort((a, b) => {
    const oa = Number(a.order_index) || 0;
    const ob = Number(b.order_index) || 0;
    return oa - ob;
  });

  const title = String(plan.title || 'Plan').trim() || 'Plan';
  const pid = String(plan.id || '').trim();
  const planType = String(plan.plan_type || '').trim();
  const planDate = String(plan.plan_date || '').trim();
  const wr = plan.workflow_run_id != null ? String(plan.workflow_run_id).trim() : '';
  const goal = planGoalBlock(plan);
  const planRiskRaw = plan.risk_level != null ? String(plan.risk_level).trim() : '';
  const planAprRaw = plan.requires_approval;
  const planApr =
    planAprRaw === undefined || planAprRaw === null ? null : Number(planAprRaw) === 1;

  const lines = [];
  lines.push(`# ${title}`);
  lines.push('');
  lines.push('## Metadata');
  lines.push('');
  lines.push(`| Field | Value |`);
  lines.push(`| --- | --- |`);
  if (pid) lines.push(`| Plan ID | \`${mdCell(pid)}\` |`);
  if (planType) lines.push(`| Type | ${mdCell(planType)} |`);
  if (planDate) lines.push(`| Date | ${mdCell(planDate)} |`);
  if (wr) lines.push(`| Workflow run | \`${mdCell(wr)}\` |`);
  if (planRiskRaw) lines.push(`| Plan risk | ${mdCell(planRiskRaw)} |`);
  if (planApr !== null) lines.push(`| Requires approval | ${planApr ? 'Yes' : 'No'} |`);
  lines.push('');

  if (goal) {
    lines.push('## Goal');
    lines.push('');
    lines.push(goal);
    lines.push('');
  }

  lines.push('## Tasks');
  lines.push('');

  if (!rawTasks.length) {
    lines.push('_No tasks yet._');
    lines.push('');
  } else {
    lines.push(`| # | Title | Status | Priority | Category | Risk | Approval | Depends on |`);
    lines.push(`| ---: | --- | --- | --- | --- | --- | :---: | --- |`);
    for (let i = 0; i < rawTasks.length; i++) {
      const t = rawTasks[i];
      const deps = parseJsonArr(t.depends_on);
      const depStr = deps.length ? deps.map((d) => `\`${d}\``).join(', ') : '—';
      lines.push(
        `| ${i + 1} | ${mdCell(t.title)} | ${mdCell(t.status)} | ${mdCell(t.priority)} | ${mdCell(t.category)} | ${mdCell(t.risk_level)} | ${Number(t.requires_approval) === 1 ? 'Yes' : 'No'} | ${depStr} |`,
      );
    }
    lines.push('');

    for (let i = 0; i < rawTasks.length; i++) {
      const t = rawTasks[i];
      const tid = String(t.id || `task_${i + 1}`);
      lines.push(`### ${i + 1}. ${String(t.title || 'Task').trim()}`);
      lines.push('');
      lines.push(`- **ID:** \`${tid}\``);
      lines.push(`- **Status:** ${String(t.status || 'todo')}`);
      lines.push(`- **Handler:** ${String(t.handler_type || 'agent')} · \`${String(t.handler_key || '').slice(0, 120)}\``);
      if (t.description) {
        lines.push('');
        lines.push(String(t.description).trim());
      }
      const files = parseJsonArr(t.files_involved);
      const routes = parseJsonArr(t.routes_involved);
      const tables = parseJsonArr(t.tables_involved);
      if (files.length || routes.length || tables.length) {
        lines.push('');
        if (routes.length) lines.push(`- **Routes:** ${routes.map(mdCell).join(', ')}`);
        if (files.length) lines.push(`- **Files:** ${files.map(mdCell).join(', ')}`);
        if (tables.length) lines.push(`- **Tables:** ${tables.map(mdCell).join(', ')}`);
      }
      if (t.blocked_reason) {
        lines.push('');
        lines.push(`> **Blocked:** ${String(t.blocked_reason).trim()}`);
      }
      lines.push('');
    }
  }

  lines.push('---');
  lines.push('');
  lines.push(`_Generated by Agent Sam · ${new Date().toISOString()}_`);
  lines.push('');

  return lines.join('\n');
}
